package com.example.lingxuan925.anif;

public class AppUser {

    String name;
    String email;

    public AppUser() {

    }

    public void setName(String name) {
        this.name = name;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }
}
